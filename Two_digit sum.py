#Two_digit sum and product calculator 
two_digit_number = input ("Enter a two digit number")
#Extracting the 1st digit
first_number = int (two_digit_number [0])
#Extracting the 2nd digit
second_number = int (two_digit_number [1])
#Now we add the two digits of two_digit_number
print ("The sum of two digits is :" , first_number + second_number)
#Product of two digits
print ("The produt of two digits is :" ,first_number + second_number)